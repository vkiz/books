package part01.chapter03;

/**
 * Тип данных double. Вычисление площади круга.
 */
class AreaDouble {
    public static void main(String[] args) {
        double pi, r, a;
        r = 10.8;
        pi = 3.1416;
        a = pi * r * r;
        System.out.println("Площадь круга равна: " + a);
    }
}
