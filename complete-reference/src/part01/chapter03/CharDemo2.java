package part01.chapter03;

/**
 * Тип данных char. Выполнение арифметических операций.
 */
class CharDemo2 {
    public static void main(String[] args) {
        char ch1 = 'X';
        System.out.println("ch1 = " + ch1); // X

        ch1++;
        System.out.println("Теперь ch1 = " + ch1); // Y
    }
}
