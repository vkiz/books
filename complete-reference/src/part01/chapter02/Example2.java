package part01.chapter02;

/**
 * Объявление переменной и присваивание ей значения.
 */
class Example2 {
    public static void main(String[] args) {
        int num;
        num = 100;
        System.out.println("Значение переменной num = " + num);
        num = num * 3;
        System.out.println("Значение переменной num * 3 = " + num);
    }
}
